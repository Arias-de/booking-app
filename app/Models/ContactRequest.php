{
    use HasFactory;

    protected $fillable = [
        'company',
        'name',
        'email',
        'phone',
        'need_type',
        'message',
        'is_read',
    ];

    protected $casts = [
        'is_read' => 'boolean',
        'created_at' => 'datetime',
    ];


}
